    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
	    String msg = name+"\n"+date+"\n"+time+"\n"+seat+"\n"+txt;
	    
	    JFileChooser fileChooser = new JFileChooser();
	    fileChooser.showOpenDialog(null);	    
            File e = fileChooser.getSelectedFile();            
            String path = e.getAbsolutePath(); 
	    String realpath = path+".png";
	    ByteArrayOutputStream out = QRCode.from(msg).to(ImageType.PNG).stream();
	    File f = new File(realpath);
	    FileOutputStream fos = new FileOutputStream(f);
	    fos.write(out.toByteArray());
	    fos.flush();
	    JOptionPane.showMessageDialog(null, "QR code succesfully generated!\nDownloaded to "+realpath);
	    count++;
	    
	}catch(Exception e){
	    JOptionPane.showMessageDialog(null, e);
	}
    }//GEN-LAST:event_jButton1ActionPerformed

